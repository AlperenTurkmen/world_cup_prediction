/**
 * Generate lib/bracketData.ts from the master WCup_2026 workbook.
 *
 * The manual-entry feature auto-derives the knockout bracket from a user's
 * predicted group standings. To do that without re-parsing Excel at runtime we
 * bake three static structures, all read straight from the workbook here:
 *
 *   1. KO_SLOT_DEFS      — for every knockout match 73..104, the two slot specs
 *                          (`1A`, `2B`, `3-ABCDF`, `W74`, `RU101`) from the
 *                          `Matches` sheet columns C/D.
 *   2. THIRD_SLOT_ORDER  — the eight R32 slots that receive a third-placed team,
 *                          in the column order of the `AssignThird` header.
 *   3. THIRD_ASSIGNMENT  — FIFA's lookup: for each of the C(12,8)=495 sorted
 *                          combinations of the eight groups whose thirds
 *                          qualified, which group's third fills each of those
 *                          eight slots.
 *
 * Run with:  npx tsx scripts/extractBracket.ts
 * It rewrites lib/bracketData.ts in place. Re-run if the workbook ever changes.
 */
import { readFileSync, writeFileSync } from "node:fs";
import * as XLSX from "xlsx";

// The eight R32 slots that take a third-placed team, in AssignThird column
// order (header row, columns D..K). Hard-coded because the header text is the
// authoritative slot spelling and is reused as-is in KO_SLOT_DEFS.
const THIRD_SLOT_ORDER = [
  "3-CEFHI",
  "3-EFGIJ",
  "3-BEFIJ",
  "3-ABCDF",
  "3-AEHIJ",
  "3-CDFGH",
  "3-DEIJL",
  "3-EHIJK",
] as const;

function cell(ws: XLSX.WorkSheet, r: number, c: number): unknown {
  const v = ws[XLSX.utils.encode_cell({ r, c })];
  return v ? v.v : undefined;
}

function main(): void {
  const url = new URL("../WCup_2026_4.2.7_en.xlsx", import.meta.url);
  const wb = XLSX.read(readFileSync(url), { type: "array" });

  // --- 1. Knockout slot definitions (matches 73..104) ----------------------
  const matches = wb.Sheets["Matches"];
  const mRange = XLSX.utils.decode_range(matches["!ref"] ?? "A1");
  const slotDefs: Record<number, { home: string; away: string }> = {};
  for (let r = mRange.s.r; r <= mRange.e.r; r++) {
    const no = cell(matches, r, 1); // B = match no.
    if (typeof no === "number" && no >= 73 && no <= 104) {
      slotDefs[no] = {
        home: String(cell(matches, r, 2)).trim(), // C
        away: String(cell(matches, r, 3)).trim(), // D
      };
    }
  }
  if (Object.keys(slotDefs).length !== 32) {
    throw new Error(`Expected 32 knockout slot defs, found ${Object.keys(slotDefs).length}.`);
  }

  // --- 2. Third-placed assignment table (495 combinations) ------------------
  const assign = wb.Sheets["AssignThird"];
  const aRange = XLSX.utils.decode_range(assign["!ref"] ?? "A1");
  const assignment: Record<string, string[]> = {};
  for (let r = aRange.s.r; r <= aRange.e.r; r++) {
    const combo = cell(assign, r, 2); // C = group combination (8 letters)
    if (typeof combo === "string" && /^[A-L]{8}$/.test(combo)) {
      const letters: string[] = [];
      for (let c = 3; c <= 10; c++) letters.push(String(cell(assign, r, c)).trim()); // D..K
      if (letters.length !== 8 || letters.some((l) => !/^[A-L]$/.test(l))) {
        throw new Error(`Malformed assignment row for ${combo}: ${letters.join(",")}`);
      }
      assignment[combo] = letters;
    }
  }
  if (Object.keys(assignment).length !== 495) {
    throw new Error(`Expected 495 assignment combinations, found ${Object.keys(assignment).length}.`);
  }

  // --- Emit lib/bracketData.ts ---------------------------------------------
  const sortedDefs = Object.keys(slotDefs)
    .map(Number)
    .sort((a, b) => a - b);
  const defsBody = sortedDefs
    .map((no) => `  ${no}: { home: ${JSON.stringify(slotDefs[no].home)}, away: ${JSON.stringify(slotDefs[no].away)} },`)
    .join("\n");

  const sortedCombos = Object.keys(assignment).sort();
  const assignBody = sortedCombos
    .map((combo) => `  ${combo}: [${assignment[combo].map((l) => `"${l}"`).join(", ")}],`)
    .join("\n");

  const out = `/**
 * GENERATED by scripts/extractBracket.ts from WCup_2026_4.2.7_en.xlsx — do not edit by hand.
 *
 * Static knockout-bracket structure used by lib/deriveBracket.ts to turn a
 * user's predicted group standings into knockout matchups. See the generator
 * for what each structure means.
 */

/** Slot spec for a knockout match side: \`1A\`, \`2B\`, \`3-ABCDF\`, \`W74\`, \`RU101\`. */
export interface KoSlotDef {
  home: string;
  away: string;
}

/** Knockout matches 73..104 → their two slot specs (from the \`Matches\` sheet). */
export const KO_SLOT_DEFS: Record<number, KoSlotDef> = {
${defsBody}
};

/** The eight R32 slots that receive a third-placed team, in assignment order. */
export const THIRD_SLOT_ORDER: readonly string[] = [
${THIRD_SLOT_ORDER.map((s) => `  ${JSON.stringify(s)},`).join("\n")}
];

/**
 * FIFA third-placed assignment table. Key = the eight groups whose thirds
 * qualified, sorted A→L (e.g. "CDEFHIJK"). Value = for each slot in
 * THIRD_SLOT_ORDER, the group whose third-placed team fills it.
 */
export const THIRD_ASSIGNMENT: Record<string, readonly string[]> = {
${assignBody}
};
`;

  const outUrl = new URL("../lib/bracketData.ts", import.meta.url);
  writeFileSync(outUrl, out);
  console.log(
    `Wrote lib/bracketData.ts: ${sortedDefs.length} KO defs, ${sortedCombos.length} assignment combos.`,
  );
}

main();
